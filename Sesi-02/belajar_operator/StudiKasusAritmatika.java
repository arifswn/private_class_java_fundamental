public class StudiKasusAritmatika {
    public static void main(String[] args) {
        int x = 20;
        int y = 40;

        // output dan keterangan
        // x == y: false
        System.out.println("x == y: " + (x == y));
        // x != y: true
        System.out.println("x != y: " + (x != y));
        // x > y: false
        System.out.println("x > y: " + (x > y));
        // x < y: true
        System.out.println("x < y: " + (x < y));
        // x >= y: false
        System.out.println("x >= y: " + (x >= y));
        // x <= y: true
        System.out.println("x <= y: " + (x <= y));

    }
}
