public class StringDataType {
    public static void main(String[] args) {
        /*
         * Tipe data string berfungsi untuk menyimpan data teks.
         * Semua huruf, karakter, dan angka bisa masuk ke dalam tipe data string
         */

        String hello = "Hello World! I'm Arif Setiawan from Indonesia";
        String age = "15";

        System.out.println(hello);
        System.out.println(age);
    }
}
