public class hello_ternary {
    public static void main(String[] args) {
        boolean suka = true;
        String jawaban;

        // menggunakan operator ternary
        jawaban = suka ? "iya" : "tidak";

        // tampilkan jawaban
        System.out.println(jawaban);

        // apa itu operator ternary?
        // operator ternary adalah operator yang memungkinkan kita untuk membuat

        // contoh lain
        int angka = 10;
        String hasil;

        // menggunakan operator ternary
        hasil = (angka % 2 == 0) ? "genap" : "ganjil";

        // tampilkan hasil
        System.out.println(hasil);
    }
}
