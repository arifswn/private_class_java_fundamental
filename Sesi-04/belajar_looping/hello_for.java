public class hello_for {
    public static void main(String[] args) {
        //for
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello World!");
        }

        //while
        int i = 0;
        while (i < 10) {
            System.out.println("Hello World!");
            i++;
        }

        //do while
        int j = 0;
        do {
            System.out.println("Hello World!");
            j++;
        } while (j < 10);
    }    
}
